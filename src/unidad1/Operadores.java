package unidad1;
/*
 * el siguiente programa  describe los operadores del lenguaje de 
 * programación en Java
 */
public class Operadores {
	public static void main(String[] args) {
		/*Los operadores Arismeticos*/
		System.out.println("Suma 2 + 2 = " + (2 + 2)); 
		System.out.println("Resta 2 - 2 = "+ (2 - 2)); 
		System.out.println("Multiplicación 2 * 2 = "+ (2 - 2));
		System.out.println("Divición 2 / 2 = "+ (2 / 2));
		System.out.println("Modulo 2 % 2 = "+ (2 % 2));
	}
}
